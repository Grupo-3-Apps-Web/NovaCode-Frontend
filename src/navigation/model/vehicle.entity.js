export class Vehicle{
    constructor( { id= '',
                     model= '',
                     brand= '',
                     year= '',
                     description= '',
                     image= '',
                     price= '',
                     rating= ''
                 }) {

        this.id = id;
        this.model = model;
        this.brand = brand;
        this.year = year;
        this.description = description;
        this.image = image;
        this.price = price;
        this.rating = rating;

    }
}